package section6ControlFlow.simpleForLoop88;

/**
 * Section 6 Control Flow - 88 A Simpler For Loop
 *
 * un exemple simple de la boucle for pour etre sur qu'on a capté
 */
class PracticeControlFlow88 {

    public static void main(String[] args) {

        for (int counter = 1; counter <= 10; counter++) {
            System.out.printf("décompte %d %n", counter);
        }
    }
}
