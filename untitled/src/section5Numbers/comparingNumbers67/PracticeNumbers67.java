package section5Numbers.comparingNumbers67;

/**
 * Section 5 - 67 Comparing Numbers
 *
 * Comparer des nbre
 * RQ souvent utilisé dans la condition d'1 if()
 */
class PracticeNumbers67 {

    public static void main(String[] args) {

        int numA = 5;
        int numB = 7;

        System.out.println(numA == numB); // == => déterminer si deux valeurs, deux nombres dans ce cas sont égaux ou non | return true ou false
        System.out.println(numA >= numB); // >= => supérieur ou égale | return true ou false
        System.out.println(numA != numB); // != => différent | return true ou false

    }
}
