package section3Learnstring.UpperLowerCasing27;

/**
 * Section 3 - Upper & Lower Casing
 *
 * mettre en Majuscule ou en minuscule une string
 */
class LearnString27 {

    public static void main(String[] args) {

        String myText ="abcdefg";
        System.out.println(myText.toUpperCase()); // toUpperCase()  => methode qui transforme un String en majuscule

        String myText2 = "ABCDEFG";
        System.out.println(myText2.toLowerCase()); // toLowerCase()  =>  methode qui transforme un String en minuscule

    }
}
